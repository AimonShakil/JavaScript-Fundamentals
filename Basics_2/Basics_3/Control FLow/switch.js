let month = 3
switch (month) {
    case 1:
        console.log("mont is Jan");
        break;
    case 2:
        console.log("mont is feb");
        break;
    case 3:
        console.log("mont is marc");
        break;
    case 4:
        console.log("mont is apr");
        break;
    case 5:
        console.log("mont is May");
        break;
    case 6:
        console.log("mont is june");
        break;

    default:
        console.log("defualt case match");
        break;
}